# Python's built-in data types with real-world examples
 
# INTEGER — whole numbers
followers = 125000         # Instagram followers
year_founded = 2008        # Year company founded
floors_in_building = 42   # Building floors
 
# FLOAT — decimal numbers
product_price = 1299.99    # Product price in rupees
temperature = 36.6         # Body temperature
rating = 4.5               # App rating
 
# STRING — text
company_name = "Infosys"
employee_email = "john@infosys.com"
address = "Bangalore, Karnataka, India"
 
# BOOLEAN — True or False only
is_verified = True         # Account verified?
has_premium = False        # Premium subscriber?
is_available = True        # Product in stock?
 
# Check the type of any variable:
print(type(followers))     # <class 'int'>
print(type(product_price)) # <class 'float'>
print(type(company_name))  # <class 'str'>
print(type(is_verified))   # <class 'bool'>
